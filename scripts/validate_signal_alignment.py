"""
Validate signal alignment between unified live scanner and backtester.

Compares signals generated by the unified live scan path (build_live_market_snapshot
→ strategy.generate_signals()) against the portfolio backtester over the same
30-day window using identical config + data.

Target: ≥90% signal overlap (same ticker, direction, DTE within 5 days).

Usage:
    PYTHONPATH=. python scripts/validate_signal_alignment.py [--days 30] [--config configs/paper_champion.yaml]
"""

